package com.andyidea.tabdemo.data;

public class Data {
	private String name;
	private String en;
	
	public Data(String name, String en) {
		this.name = name;
		this.en = en;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEn() {
		return en;
	}
	public void setEn(String en) {
		this.en = en;
	}
	
}
